import random

def magic_8_ball():
    answers = [
        "Бесспорно",
        "Предрешено",
        "Никаких сомнений",
        "Определённо да",
        "Можешь быть уверен в этом",
        "Мне кажется — да",
        "Вероятнее всего",
        "Хорошие перспективы",
        "Знаки говорят — да",
        "Да",
        "Пока не ясно, попробуй снова",
        "Спроси позже",
        "Лучше не рассказывать",
        "Сейчас нельзя предсказать",
        "Сконцентрируйся и спроси опять",
        "Даже не думай",
        "Мой ответ — нет",
        "По моим данным — нет",
        "Перспективы не очень хорошие",
        "Весьма сомнительно"
    ]

    print("Привет! Я магический шар 8, и я знаю ответ на любой твой вопрос.")
    
    while True:
        question = input("Задай свой вопрос (или напиши 'выход', чтобы закончить): ")
        
        if question.lower() == 'выход':
            print("Возвращайся, если возникнут вопросы!")
            break
        
        if question.strip() == "":
            print("Кажется, ты ничего не спросил. Попробуй еще раз!")
            continue
        
        answer = random.choice(answers)
        print(f"Магический шар говорит: {answer}\n")

if __name__ == "__main__":
    magic_8_ball()